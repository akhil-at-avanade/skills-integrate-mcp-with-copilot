document.addEventListener("DOMContentLoaded", () => {
  const activitiesList = document.getElementById("activities-list");
  const activitySelect = document.getElementById("activity");
  const signupForm = document.getElementById("signup-form");
  const messageDiv = document.getElementById("message");
  const teacherStatus = document.getElementById("teacher-status");
  const loginBtn = document.getElementById("login-btn");
  const logoutBtn = document.getElementById("logout-btn");
  const loginModal = document.getElementById("teacher-login-modal");
  const loginSubmit = document.getElementById("teacher-login-submit");
  const loginCancel = document.getElementById("teacher-login-cancel");
  const loginMessage = document.getElementById("teacher-login-message");
  let teacherUsername = null;

  function updateAuthUI() {
    if (teacherUsername) {
      teacherStatus.textContent = `Logged in as ${teacherUsername}`;
      loginBtn.classList.add("hidden");
      logoutBtn.classList.remove("hidden");
      signupForm.querySelector("button[type='submit']").disabled = false;
      document.querySelectorAll(".delete-btn").forEach(btn => btn.disabled = false);
    } else {
      teacherStatus.textContent = "Not logged in";
      loginBtn.classList.remove("hidden");
      logoutBtn.classList.add("hidden");
      signupForm.querySelector("button[type='submit']").disabled = true;
      document.querySelectorAll(".delete-btn").forEach(btn => btn.disabled = true);
    }
  }

  loginBtn.addEventListener("click", () => {
    loginModal.classList.remove("hidden");
    loginMessage.textContent = "";
  });

  loginCancel.addEventListener("click", () => {
    loginModal.classList.add("hidden");
    loginMessage.textContent = "";
  });

  loginSubmit.addEventListener("click", async () => {
    const username = document.getElementById("teacher-username").value;
    const password = document.getElementById("teacher-password").value;
    try {
      const response = await fetch("/teacher/login", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ username, password })
      });
      const result = await response.json();
      if (response.ok) {
        teacherUsername = username;
        loginModal.classList.add("hidden");
        updateAuthUI();
      } else {
        loginMessage.textContent = result.detail || "Login failed";
      }
    } catch (err) {
      loginMessage.textContent = "Login error";
    }
  });

  logoutBtn.addEventListener("click", async () => {
    if (!teacherUsername) return;
    await fetch("/teacher/logout", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ username: teacherUsername })
    });
    teacherUsername = null;
    updateAuthUI();
  });

  // Function to fetch activities from API
  async function fetchActivities() {
    try {
      const response = await fetch("/activities");
      const activities = await response.json();

      activitiesList.innerHTML = "";
      activitySelect.innerHTML = '<option value="">-- Select an activity --</option>';

      Object.entries(activities).forEach(([name, details]) => {
        const activityCard = document.createElement("div");
        activityCard.className = "activity-card";
        const spotsLeft = details.max_participants - details.participants.length;
        const participantsHTML =
          details.participants.length > 0
            ? `<div class="participants-section">
              <h5>Participants:</h5>
              <ul class="participants-list">
                ${details.participants
                  .map(
                    (email) =>
                      `<li><span class="participant-email">${email}</span><button class="delete-btn" data-activity="${name}" data-email="${email}">74c</button></li>`
                  )
                  .join("")}
              </ul>
            </div>`
            : `<p><em>No participants yet</em></p>`;
        activityCard.innerHTML = `
          <h4>${name}</h4>
          <p>${details.description}</p>
          <p><strong>Schedule:</strong> ${details.schedule}</p>
          <p><strong>Availability:</strong> ${spotsLeft} spots left</p>
          <div class="participants-container">
            ${participantsHTML}
          </div>
        `;
        activitiesList.appendChild(activityCard);
        const option = document.createElement("option");
        option.value = name;
        option.textContent = name;
        activitySelect.appendChild(option);
      });
      document.querySelectorAll(".delete-btn").forEach((button) => {
        button.addEventListener("click", handleUnregister);
      });
      updateAuthUI();
    } catch (error) {
      activitiesList.innerHTML = "<p>Failed to load activities. Please try again later.</p>";
      console.error("Error fetching activities:", error);
    }
  }

  async function handleUnregister(event) {
    const button = event.target;
    const activity = button.getAttribute("data-activity");
    const email = button.getAttribute("data-email");
    if (!teacherUsername) {
      messageDiv.textContent = "Only teachers can unregister students.";
      messageDiv.className = "error";
      messageDiv.classList.remove("hidden");
      setTimeout(() => { messageDiv.classList.add("hidden"); }, 5000);
      return;
    }
    try {
      const response = await fetch(
        `/activities/${encodeURIComponent(activity)}/unregister?email=${encodeURIComponent(email)}&teacher=${encodeURIComponent(teacherUsername)}`,
        { method: "DELETE" }
      );
      const result = await response.json();
      if (response.ok) {
        messageDiv.textContent = result.message;
        messageDiv.className = "success";
        fetchActivities();
      } else {
        messageDiv.textContent = result.detail || "An error occurred";
        messageDiv.className = "error";
      }
      messageDiv.classList.remove("hidden");
      setTimeout(() => { messageDiv.classList.add("hidden"); }, 5000);
    } catch (error) {
      messageDiv.textContent = "Failed to unregister. Please try again.";
      messageDiv.className = "error";
      messageDiv.classList.remove("hidden");
      console.error("Error unregistering:", error);
    }
  }

  signupForm.addEventListener("submit", async (event) => {
    event.preventDefault();
    if (!teacherUsername) {
      messageDiv.textContent = "Only teachers can register students.";
      messageDiv.className = "error";
      messageDiv.classList.remove("hidden");
      setTimeout(() => { messageDiv.classList.add("hidden"); }, 5000);
      return;
    }
    const email = document.getElementById("email").value;
    const activity = document.getElementById("activity").value;
    try {
      const response = await fetch(
        `/activities/${encodeURIComponent(activity)}/signup?email=${encodeURIComponent(email)}&teacher=${encodeURIComponent(teacherUsername)}`,
        { method: "POST" }
      );
      const result = await response.json();
      if (response.ok) {
        messageDiv.textContent = result.message;
        messageDiv.className = "success";
        signupForm.reset();
        fetchActivities();
      } else {
        messageDiv.textContent = result.detail || "An error occurred";
        messageDiv.className = "error";
      }
      messageDiv.classList.remove("hidden");
      setTimeout(() => { messageDiv.classList.add("hidden"); }, 5000);
    } catch (error) {
      messageDiv.textContent = "Failed to sign up. Please try again.";
      messageDiv.className = "error";
      messageDiv.classList.remove("hidden");
      console.error("Error signing up:", error);
    }
  });

  fetchActivities();
});
